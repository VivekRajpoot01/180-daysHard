// To print the numbers and character variable
#include <iostream>
using namespace std;

int main() {
    int num1;
    cout << "Enter num1: ";
    cin >> num1;
    cout << "Value of num1: " << num1 << endl;

    double num2;
    cout << "Enter num2: ";
    cin >> num2;
    cout << "Value of num2: " << num2 << endl;

    char c;
    cout << "Enter a character: ";
    cin >> c;
    cout << "Value of character: " << c << endl;

    return 0;
}

